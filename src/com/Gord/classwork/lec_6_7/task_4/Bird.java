package com.Gord.classwork.lec_6_7.task_4;

public class Bird {
    String nameBird;
    int sizeBird;

    public Bird( int sizeBird) {
        this.nameBird = nameBird;
        this.sizeBird = sizeBird;
    }

    public String getNameBird() {
        return nameBird;
    }

    public void setNameBird(String nameBird) {
        this.nameBird = nameBird;
    }

    public int getSizeBird() {
        return sizeBird;
    }

    public void setSizeBird(int sizeBird) {
        this.sizeBird = sizeBird;
    }



}
